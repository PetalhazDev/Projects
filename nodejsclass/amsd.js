function addNumber(number1,number2){
    return Number(number1) + Number(number2);

}

function subtractNumber(number1,number2){
    return Number(number1) * Number(number2)
}

const multipleNumber = (number1,number2) =>{
    return Number(number1) * Number(number2)

}

var divideNumber = (number1,number2) => {
    return Number(number1) / Number(number2)

}



