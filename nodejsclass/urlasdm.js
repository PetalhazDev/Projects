
function addNumber(number1,number2){
    return Number(number1) + Number(number2);

}
module.exports.addition = addNumber;

exports.subtractNumbers = function(number1,number2){
    return Number(number1) - Number(number2)
}



exports.multiplyNumbers = function(number1,number2){
    return Number(number1) * Number(number2)
}



exports.divideNumbers = function(number1,number2){
    return Number(number1) / Number(number2)
}